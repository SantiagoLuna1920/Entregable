package com.entregable;

public class EntregableDatosJava {
    public static void main (String[] args) {
        byte number1 = 1;
        short number2 = 12;
        int number3 = 1234;
        long number4 = 12345678;
        float number5 = 4.9f;
        double number6 = 4.99d;
        boolean stateTrue = true;
        boolean stateFalse = false;
        String name = "Santiago Luna";
//datos
        System.out.println("Tipo de datos en java");

        System.out.println("numericos.");

        System.out.println("number1= " + number1);
        System.out.println("number2= " + number2);
        System.out.println("number3= " + number3);
        System.out.println("number4= " + number4);
        System.out.println("number5= " + number5);
        System.out.println("number6= " + number6);

        System.out.println("Datos booleanos.");
        System.out.println("Verdadero= " + stateTrue);
        System.out.println("Falso= " + stateFalse);

        System.out.println("Cadenas.");
        System.out.println("nombre= " + name);

    }
}
